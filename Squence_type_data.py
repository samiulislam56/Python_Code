#List Type Data

Li = ['Sami' , 'shehab' , 'Rayhan', 'Naeem']
print(Li);
print(type(Li));

#Mutable Change

Li = ['Sami' , 'shehab' , 'Rayhan', 'Naeem']
Li[2] = 'Oshiur'
print(Li)

#Tuple Type Data

tup = (10,12,45,60) #immutable
print(tup)
print(type(tup))

tup1 = ('Sami' , 'shehab' , 'Rayhan', 'Naeem')
print(tup1)

#Range type data

ran = range(6)
print(ran)

for i in ran:
    print(i)




